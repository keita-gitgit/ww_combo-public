const ACTION_KIND_ORDER: ActionKind[] = [
  'normal',
  'skill',
  'liberation',
  'forte',
  'echo',
  'concerto',
  'move',
]

// 旧版で表示していたキャラ別の詳細技名。保存済みデータを基本技へ
// 安全に移行するためにだけ使用し、新規の技パレットには表示しない。
export function getLegacyCharacterActionTemplate(name: string): ActionTemplate[] {
  const groups = CHARACTER_ACTION_GROUPS[name]
  if (!groups) return []

  const detailedActions = ACTION_KIND_ORDER.flatMap((kind) =>
    (groups[kind] ?? []).map((actionName) => ({ name: actionName, kind })),
  )
  const normalFamilies = new Map<string, number>()

  const candidates = detailedActions.map((action) => {
    const normalizedName = action.name.replace(/通常攻撃/g, '通常')
    const normalSequence =
      !normalizedName.includes('強化通常') && normalizedName.match(/^(.*通常.*?)(\d+)$/)

    if (normalSequence) {
      const family = normalSequence[1]
      let familyIndex = normalFamilies.get(family)
      if (familyIndex === undefined) {
        familyIndex = normalFamilies.size
        normalFamilies.set(family, familyIndex)
      }
      return {
        ...action,
        compactBase:
          familyIndex === 0
            ? '通常'
            : `通常${String.fromCharCode('A'.charCodeAt(0) + familyIndex)}`,
        alwaysNumbered: true,
      }
    }

    let compactBase: string
    if (action.name === '音骸' || action.name === '回避' || action.name === 'ジャンプ') {
      compactBase = action.name
    } else if (action.name.startsWith('変奏')) {
      compactBase = '変奏'
    } else if (action.name.startsWith('終奏')) {
      compactBase = '終奏'
    } else if (action.name.includes('強化スキル')) {
      compactBase = '強化スキル'
    } else if (action.name.includes('強化通常')) {
      compactBase = '強化通常'
    } else if (action.name.includes('回避反撃')) {
      compactBase = '回避反撃'
    } else if (/(空中|落下|崩れ落ち)/.test(action.name)) {
      compactBase = '空中'
    } else if (action.name.includes('重撃')) {
      compactBase = '重撃'
    } else if (action.name.includes('照準')) {
      compactBase = '照準'
    } else if (action.kind === 'skill') {
      compactBase = 'スキル'
    } else if (action.kind === 'liberation') {
      compactBase = '解放'
    } else if (action.kind === 'normal') {
      compactBase = '特殊通常'
    } else if (action.kind === 'forte') {
      // 短い固有用語（ゲージ名・構え名など）は識別しやすいため残す。
      // 長い技名だけを「固有N」にまとめ、パレットの横幅を抑える。
      compactBase = Array.from(action.name).length <= 7 ? action.name : '固有'
    } else if (action.kind === 'echo') {
      compactBase = '音骸'
    } else if (action.kind === 'move') {
      compactBase = '移動'
    } else {
      compactBase = action.name
    }

    return { ...action, compactBase, alwaysNumbered: false }
  })

  const totals = candidates.reduce<Map<string, number>>((counts, action) => {
    counts.set(action.compactBase, (counts.get(action.compactBase) ?? 0) + 1)
    return counts
  }, new Map())
  const seen = new Map<string, number>()
  const usedNames = new Set<string>()

  return candidates.map((action) => {
    const ordinal = (seen.get(action.compactBase) ?? 0) + 1
    seen.set(action.compactBase, ordinal)
    const needsNumber = action.alwaysNumbered || (totals.get(action.compactBase) ?? 0) > 1
    let compactName = `${action.compactBase}${needsNumber ? ordinal : ''}`

    // 既存の短い固有名と生成した省略名が万一重なっても、キャラ内では一意にする。
    let duplicateOrdinal = 2
    while (usedNames.has(compactName)) {
      compactName = `${action.compactBase}${duplicateOrdinal}`
      duplicateOrdinal += 1
    }
    usedNames.add(compactName)

    return {
      name: compactName,
      kind: action.kind,
      ...(compactName === action.name ? {} : { officialName: action.name }),
    }
  })
}

